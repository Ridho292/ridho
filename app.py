def hitungLuasSegitiga(a,t):
    return round(0.5 * a * t,2)

alas = float(input("input alas segitiga :"))
tinggi = float(input("input tinggi segitiga :"))

print("luas segitiga = ", hitungLuasSegitiga(alas,tinggi))
